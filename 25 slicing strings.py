# Slicing strings

#You can return a range of characters by using the slicing syntax

#Specify the start index and the end index
#separated  by a colon, to return  a part of the string.


#Get the characters from positioning 2 to position 5 (not included )

b = "Hello, World!"
print(b[2:5]) #stop index is not added
print(b[7:12]) # to get world