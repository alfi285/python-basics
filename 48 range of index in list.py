# Range of indexes

#You can  specify a range of indexes by
# specifying where to start and where to end the range.

# When specifying a range ,
# the return value will be a new list with the specified items

# Return the third, fourth, and fifth item

thislist = ["violet", "indigo", "blue", "green", "yellow", "orange", "red"]

print(thislist[2:5])
